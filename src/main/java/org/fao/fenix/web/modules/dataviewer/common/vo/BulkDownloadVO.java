package org.fao.fenix.web.modules.dataviewer.common.vo;

import java.util.Date;

import com.google.gwt.user.client.rpc.IsSerializable;

public class BulkDownloadVO implements IsSerializable {

	private Long moduleID;
	
	private String domainLabel;
	
	private String domainCode;
	
	private Date creationDate;
	
	private String filename;
	
	private String folder;
	
	private String content;
	
	private int tabOrder;
	
	private String lang;
	
	private String url;

	public Long getModuleID() {
		return moduleID;
	}

	public void setModuleID(Long moduleID) {
		this.moduleID = moduleID;
	}

	public String getDomainLabel() {
		return domainLabel;
	}

	public void setDomainLabel(String domainLabel) {
		this.domainLabel = domainLabel;
	}

	public String getDomainCode() {
		return domainCode;
	}

	public void setDomainCode(String domainCode) {
		this.domainCode = domainCode;
	}

	public Date getCreationDate() {
		return creationDate;
	}

	public void setCreationDate(Date creationDate) {
		this.creationDate = creationDate;
	}

	public String getFilename() {
		return filename;
	}

	public void setFilename(String filename) {
		this.filename = filename;
	}

	public String getFolder() {
		return folder;
	}

	public void setFolder(String folder) {
		this.folder = folder;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public int getTabOrder() {
		return tabOrder;
	}

	public void setTabOrder(int tabOrder) {
		this.tabOrder = tabOrder;
	}

	public String getLang() {
		return lang;
	}

	public void setLang(String lang) {
		this.lang = lang;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	@Override
	public String toString() {
		return "BulkDownloadVO [content=" + content + ", creationDate=" + creationDate + ", domainCode=" + domainCode + ", domainLabel=" + domainLabel + ", filename=" + filename + ", folder=" + folder + ", lang=" + lang + ", moduleID=" + moduleID
				+ ", tabOrder=" + tabOrder + ", url=" + url + "]";
	}
	
}