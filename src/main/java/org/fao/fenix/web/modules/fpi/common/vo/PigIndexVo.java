package org.fao.fenix.web.modules.fpi.common.vo;

import java.util.Date;

import com.google.gwt.user.client.rpc.IsSerializable;

public class PigIndexVo implements IsSerializable {

	private double pigWeightedPrice;
	
	private Date date;

	public double getPigWeightedPrice() {
		return pigWeightedPrice;
	}

	public void setPigWeightedPrice(double pigWeightedPrice) {
		this.pigWeightedPrice = pigWeightedPrice;
	}

	public Date getDate() {
		return date;
	}

	public void setDate(Date date) {
		this.date = date;
	}
	
}
