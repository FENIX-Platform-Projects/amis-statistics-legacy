package org.fao.fenix.web.modules.latex.server;

public class LatexStyleSettings {

	private String caption = "";
	
	private String captionPosition = "";
	
	private String captionColor = "";
	
	private String captionFontFamily = "";
	
	private String captionFontSize = "";
	
	private String boxLineColor = "";
	
	private String boxBackgroundColor = "";

	public String getCaption() {
		return caption;
	}

	public void setCaption(String caption) {
		this.caption = caption;
	}

	public String getCaptionPosition() {
		return captionPosition;
	}

	public void setCaptionPosition(String captionPosition) {
		this.captionPosition = captionPosition;
	}

	public String getCaptionColor() {
		return captionColor;
	}

	public void setCaptionColor(String captionColor) {
		this.captionColor = captionColor;
	}

	public String getCaptionFontFamily() {
		return captionFontFamily;
	}

	public void setCaptionFontFamily(String captionFontFamily) {
		this.captionFontFamily = captionFontFamily;
	}

	public String getCaptionFontSize() {
		return captionFontSize;
	}

	public void setCaptionFontSize(String captionFontSize) {
		this.captionFontSize = captionFontSize;
	}

	public String getBoxLineColor() {
		return boxLineColor;
	}

	public void setBoxLineColor(String boxLineColor) {
		this.boxLineColor = boxLineColor;
	}

	public String getBoxBackgroundColor() {
		return boxBackgroundColor;
	}

	public void setBoxBackgroundColor(String boxBackgroundColor) {
		this.boxBackgroundColor = boxBackgroundColor;
	}
	
}