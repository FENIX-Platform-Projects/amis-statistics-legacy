package org.fao.fenix.web.modules.core.common.vo.highcharts;

import com.google.gwt.user.client.rpc.IsSerializable;

public class HighChartXAxis implements IsSerializable {
	
	
	// this the default for the bar charts 
	Integer staggerLines = 2;
	
	String fontSize = "10px";
	
	Integer rotation = 0;
	
	// distance to Y
	Integer y = 0;

	
	
	public void setTimeserieDefault(HighChartXAxis axis) {
		axis.setStaggerLines(1);
		axis.setRotation(-45);
		axis.setY(20);
	}


	public Integer getStaggerLines() {
		return staggerLines;
	}


	public void setStaggerLines(Integer staggerLines) {
		this.staggerLines = staggerLines;
	}


	public String getFontSize() {
		return fontSize;
	}


	public void setFontSize(String fontSize) {
		this.fontSize = fontSize;
	}


	public Integer getRotation() {
		return rotation;
	}


	public void setRotation(Integer rotation) {
		this.rotation = rotation;
	}


	public Integer getY() {
		return y;
	}


	public void setY(Integer y) {
		this.y = y;
	}
	
	

}
