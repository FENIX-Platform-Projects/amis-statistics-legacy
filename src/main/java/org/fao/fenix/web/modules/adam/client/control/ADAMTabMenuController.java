package org.fao.fenix.web.modules.adam.client.control;

import org.fao.fenix.web.modules.adam.common.enums.ADAMCurrentVIEW;

public class ADAMTabMenuController {

	public static String getPosition(final ADAMCurrentVIEW currentView){
	    String position = "";
		
		switch (currentView) {
		case HOME: 	
			position = "TAB1";
			break;
		case BROWSE: 	
			position = "TAB2";
			break;
		case ANALYSE: 	
			position = "TAB3";
			break;
		case PROFILES: 	
			position = "TAB4";
			break;
		case LINKS: 	
			position = "TAB5";
			break;
		}
		
		return position;
	}

	
}