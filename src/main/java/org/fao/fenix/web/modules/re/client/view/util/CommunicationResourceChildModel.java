package org.fao.fenix.web.modules.re.client.view.util;

import org.fao.fenix.web.modules.re.common.vo.ResourceChildModel;

public class CommunicationResourceChildModel extends ResourceChildModel {

	public CommunicationResourceChildModel(String name) {
		super(name);
	}
	
	public void setLocalId(String localId) {
		set("localId", localId);
	}
	
	public String getLocalId() {
		return get("localId");
	}
	
	public void setDigest(String digest) {
		set("digest", digest);
	}
	
	public String getDigest() {
		return get("digest");
	}
	
	public void setHost(String host) {
		set("host", host);
	}
	
	public String getHost() {
		return get("host");
	}
	
	public void setHostLabel(String hostLabel) {
		set("hostLabel", hostLabel);
	}
	
	public String getHostLabel() {
		return get("hostLabel");
	}
	
}