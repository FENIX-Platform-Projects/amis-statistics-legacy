package org.fao.fenix.web.modules.birt.server.utils.CountryBriefTemplate;

import java.util.ArrayList;
import java.util.List;

public class CropLegend {
	
	String pathMapFile;
	List<String> codeCropLayers;
	List<String> cropName;
	
	public CropLegend(){
		codeCropLayers = new ArrayList<String>();
		cropName = new ArrayList<String>();
	}
	
	public String getPathMapFile() {
		return pathMapFile;
	}
	
	public void setPathMapFile(String pathMapFile) {
		this.pathMapFile = pathMapFile;
	}
	
	public List<String> getCodeCropLayers() {
		return codeCropLayers;
	}
	
	public void addSetCodeCropLayers(String codeCropLayer) {
		this.codeCropLayers.add(codeCropLayer);
	}
	
	public List<String> getCropName() {
		return cropName;
	}
	
	public void addSetCropName(String cropName) {
		this.cropName.add(cropName);
	}

}
