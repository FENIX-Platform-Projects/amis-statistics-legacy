package org.fao.fenix.web.modules.sldeditor.common.vo;

import java.util.List;

public class SldEditorUtils {

	private static String SldNameFile;
	
	private static String SldPathFile;
	
	public static List<SLDEditorRuleVO> voForInterface;
	
	public static String sldType;
	
	public static boolean creatingInterface;
	
	public SldEditorUtils()
	{
		SldEditorUtils.SldNameFile = new String();
		SldEditorUtils.SldPathFile = new String();
		creatingInterface = false;
	}
			
	public static String getSldNameFile() {
		return SldNameFile;
	}

	public static void setSldNameFile(String sldNameFile) {
		SldNameFile = sldNameFile;
	}

	public static String getSldPathFile() {
		return SldPathFile;
	}

	public static void setSldPathFile(String sldPathFile) {
		SldPathFile = sldPathFile;
	}

	public static List<SLDEditorRuleVO> getVoForInterface() {
		return voForInterface;
	}

	public static void setVoForInterface(List<SLDEditorRuleVO> voForInterface) {
		SldEditorUtils.voForInterface = voForInterface;
	}

	public static String getSldType() {
		return sldType;
	}

	public static void setSldType(String sldType) {
		SldEditorUtils.sldType = sldType;
	}

	public static boolean isCreatingInterface() {
		return creatingInterface;
	}

	public static void setCreatingInterface(boolean creatingInterface) {
		SldEditorUtils.creatingInterface = creatingInterface;
	}	
}
