package org.fao.fenix.web.modules.adam.client.view.profile;


public class FormatUtils {
	
	private final static String BLUE = "#589BCC"; 
	public final static String BREAK = "<br/><br/>";
	public final static String SPACER = "&nbsp;&nbsp;";
	

	 public static String getFormattedTitle(String title, boolean isCentered){
		  if(isCentered)
			  return "<font color='"+BLUE+"'><b><center>"+title+"</center></b></font>";  
		  else	   
			  return "<font color='"+BLUE+"'><b>"+title+"</b></font>";
	  }
	 
	 public static String getFormattedTitle(String title, boolean isCentered, String colour){
		 if(isCentered)
			  return "<font color='"+colour+"'><b><center>"+title+"</center></b></font>";  
		  else	   
			  return "<font color='"+colour+"'><b>"+title+"</b></font>";
	  }
	 
	 public static String getFormattedTitle(String title){
		  return "<font color='#000000'><b>"+title+"</b></font>";
	  }
}
